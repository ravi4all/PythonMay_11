word = "Machine Learning"

# Slicing and Indexing
# letter at 0 index
print(word[0])

# Last letter
print(word[-1])

# word[lowerbound : upperbound ]
# lowerbound is included and upperbound is excluded
print(word[0:7])

# except last index full string
print(word[0:-1])

# print full string
print(word[:])

# print(word[0:])
# print(word[:-1])

print(word[0:10:2])

# Reverse of a string
print(word[::-1])